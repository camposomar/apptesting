  <p class="pink-head" style="font-size:14px; color:#E0630E;">Bajo el capó</p>
            <p class="desc">Mientras diseñábamos Synapse queríamos estar absolutamente seguros que al menos resolviera los problemas anteriormente mencionados, después de un año de desarrollo, Synapse fue puesto a prueba y poco después de eso reemplazó nuestra solución existente de monitoreo. No sólo fuimos capaces de alcanzar todas las metas que nos propusimos sino que fueron superadas por mucho. En base a las pruebas contra otros sistemas de monitoreo, Synapse los supero con velocidades de rastreo entre 200-500%, por ejemplo, Synapse es capaz de realizar 13,000 chequeos en apenas 30 segundos en un sólo servidor.</p>

			<p class="desc">El corazón de Synapse es un sistema eficiente de procesos y un mecanismo de cola, el servicio de procesos permite a Synapse realizar un gran número de revisiones simultaneas, mientras que el mecanismo de cola proporciona un método para que múltiples nodos de Synapse se comporten correctamente cuando corre en un modo distribuido. Esas dos características de diseño permiten escalar a Synapse muy rápido y de forma dinámica sin ningún cambio de configuración ni interrupciones.</p>

			<p class="desc">Mientras que el rendimiento es importante, el almacenamiento de datos es igual de significativo. Synapse utiliza un motor SQL para todas sus transacciones. Esto incluye el almacenamiento de todas las configuraciones del servicio, eventos y datos históricos. Manteniendo un mecanismo de almacenamiento unificado también permite a otras aplicaciones acceder a la información de Synapse.</p>



			<p class="desc">En el front-end es una aplicación basada en PHP y Ajax que categoriza y muestra todos los eventos basados en la gravedad del problema. Esta misma interface sirve como plataforma para configurar las opciones del monitoreo de servicios y consultar datos históricos. Nuestros administradores de servidores monitorean Synapse 24/7/365 para asegurarse que tus servidores estan funcionando de manera óptima en todo momento.</p>

