        Sobre Synsapse</p>
            <p class="desc">Synapse es un sistema multiproceso, distribuido y de alto desempeño orientado a monitorear servidores, desarrollado in-house por Daniel Selans, Mark Gregan y Philip Jameson. Desde su base, Synapse fue diseñado como una aplicación de clase empresarial que es capaz de ser escalada y realizar decenas de miles de transacciones por segundo. Synapse utiliza un algoritmo inteligente para determinar cuando un servidor no responde, está sobrecargado o está filtrando las conexiones entrantes. A la fecha, Synapse ha monitoreado los servidores de nuestro centro de datos en intervalos de aproximadamente 30 segundos.</p>
